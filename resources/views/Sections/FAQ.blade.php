                 <h5 class="font_w500 w_100">
                    To reapply for your B1/B2 visa, which is due to expire, you may be eligible for the interview waiver program if certain conditions are met. The process is the same as applying for a new visa, except that you do not have to appear for an interview; instead, you are required to submit the following documents at Nepal Investment Mega Bank, Durbar Marg:
                    <br><br><ul>
                        <li>DS-160 Confirmation Page</li>
                        <li>Interview Waiver Letter (X2 copies)</li>
                        <li>Original & Valid Passport (Old passport with issued visa if any)</li>
                        <li>Visa Fee Payment Receipt</li>
                        <li>2x2 Photo</li>
                        <li>Visa Copy</li>
                    </ul>
