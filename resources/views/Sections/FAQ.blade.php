                <h4 class="font_w500 w_100 question_title" >What is an I-20 and how do I get it ? </h4>
                <h3 class="font_w500 w_100 flex justify_fe"><i class="ri-add-line"></i></h3>
            </div>
             <div class="answer w_100 grid col_middle_3 align_c justify_sb">
                <h3 class="font_w500 w_100 question_title"></h3>
                <h5 class="font_w500 w_100">The Form I-20 is an official U.S. Government form, issued by a certified school, which a prospective nonimmigrant student must have in order to get an F-1 or M-1 visa. Form I-20 acts as proof-of-acceptance and contains the information necessary to pay the SEVIS I-901 fee, apply for a visa or change visa status, and be admitted into the United States. The Form I-20 has the student's SEVIS identification number, which starts with the letter N and is followed by nine digits, on the upper right side directly above the barcode.</h5>
